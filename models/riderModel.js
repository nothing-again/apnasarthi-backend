import mongoose from "mongoose";

const riderSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const Rider = mongoose.model("Rider", riderSchema);

export default Rider;
